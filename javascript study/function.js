function divideFunction(p1,p2){
    return p1/p2;
};
function addFunction(p1,p2){
    return p1+p2;
}
function minusFunction(p1,p2){
    return p1-p2;
}
function multiFunction(p1,p2){
    return p1*p2;
}
console.log(addFunction(1,2))
console.log(minusFunction(10,5))
console.log(multiFunction(3,4))
console.log(divideFunction(10,5))